//Switch statement
let day = "Monday";
switch (day) {
    case "Monday":
        console.log("Start of the workweek.");
        break;
    case "Wednesday":
        console.log("Midweek day.");
        break;
    case "Friday":
        console.log("Weekend is near!");
        break;
    default:
        console.log("It's just another day.");
}