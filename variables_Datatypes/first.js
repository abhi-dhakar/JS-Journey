// Variables and Data Types in JavaScript

// 1. String (Text)
let name = "Abhishek";
console.log("Name:", name);

// 2. Number (Integer & Float)
let age = 20;
let pi = 3.1415;
console.log("Age:", age);
console.log("Value of Pi:", pi);

// 3. Boolean (True/False)
let isStudent = true;
console.log("Is Student:", isStudent);

// 4. Undefined (Variable declared but not assigned a value)
let futureGoal;
console.log("Future Goal:", futureGoal);

// 5. Null (Explicitly no value)
let emptyValue = null;
console.log("Empty Value:", emptyValue);

// 6. Object (Key-Value Pair)
let person = {
  firstName: "Abhishek",
  lastName: "dhakar",
  age: 20,
  isStudent: true,
};
console.log("Person Object:", person);

// 7. Array (List of values)
let subjects = ["Math", "Physics", "JavaScript"];
console.log("Subjects:", subjects);

// 8. BigInt (For large numbers)
let bigNumber = 12345678901234567890n;
console.log("Big Number:", bigNumber);

// 9. Symbol (Unique identifier)
let uniqueID = Symbol("id");
console.log("Unique ID:", uniqueID);
